  uint16_t CRC_EPC = CRC16_CalculateBuf(RawData, sizeof(RawData));
  
  uint8_t crcHigh = (CRC_EPC >> 8) & 0xFF; // MSB
  uint8_t crcLow = CRC_EPC & 0xFF;         // LSB

  uint8_t SendData[sizeof(RawData) + 2 + 2]; // 2 bayt başlık, 2 bayt CRC
  
